fun main() {
    var myName = "Snehil"

    // To reverse the given String
    println("My name is: ${myName.reversed()}")

    // To convert lowercase strings to uppercase
    println("My name is: ${myName.uppercase()}")
    myName = "SNEHIL"

    // To convert the uppercase strings to lowercase
    println("My name is: ${myName.lowercase()}")
}